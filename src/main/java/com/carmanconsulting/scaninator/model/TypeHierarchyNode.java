package com.carmanconsulting.scaninator.model;

public abstract class TypeHierarchyNode implements Comparable<TypeHierarchyNode>
{
//----------------------------------------------------------------------------------------------------------------------
// Fields
//----------------------------------------------------------------------------------------------------------------------

    protected final String typeName;

//----------------------------------------------------------------------------------------------------------------------
// Constructors
//----------------------------------------------------------------------------------------------------------------------

    TypeHierarchyNode(String typeName)
    {
        this.typeName = typeName;
    }

//----------------------------------------------------------------------------------------------------------------------
// Comparable Implementation
//----------------------------------------------------------------------------------------------------------------------

    
    @Override
    public int compareTo(TypeHierarchyNode o)
    {
        return typeName.compareTo(o.typeName);
    }

//----------------------------------------------------------------------------------------------------------------------
// Getter/Setter Methods
//----------------------------------------------------------------------------------------------------------------------

    public String getTypeName()
    {
        return typeName;
    }

//----------------------------------------------------------------------------------------------------------------------
// Canonical Methods
//----------------------------------------------------------------------------------------------------------------------

    public String toString()
    {
        return getTypeName();
    }
}
